def _text(value: dict | str | list, language: str):
    if isinstance(value, dict):
        return value.get(language) or value.get("uk") or ""
    return value


def _html_paragraphs(value: dict[str, list[str]], language: str) -> str:
    return "".join(f"<p>{paragraph}</p>" for paragraph in _text(value, language))


def _html_list(items: dict[str, list[str]], language: str) -> str:
    return "<ul>" + "".join(f"<li>{item}</li>" for item in _text(items, language)) + "</ul>"


def _localized_html(article: dict, language: str) -> str:
    pieces = [
        '<div class="article-mini-landing">',
        f"<h2>{_text(article['title'], language)}</h2>",
        f"<p>{_text(article['excerpt'], language)}</p>",
        "</div>",
        f"<h2>{_text(article['scenario_title'], language)}</h2>",
        '<div class="article-scenario-grid">',
    ]
    for title, body in article["scenarios"]:
        pieces.append(f"<div><h3>{_text(title, language)}</h3><p>{_text(body, language)}</p></div>")
    pieces.append("</div>")

    for heading, paragraphs in article["sections"]:
        pieces.append(f"<h2>{_text(heading, language)}</h2>")
        pieces.append(_html_paragraphs(paragraphs, language))

    pieces.extend(
        [
            '<div class="article-checklist-grid">',
            f"<h2>{_text(article['unique_title'], language)}</h2>",
            _html_list(article["unique_items"], language),
            "</div>",
            "<h2>Як це робить TwoComms</h2>" if language == "uk" else "<h2>Как это делает TwoComms</h2>",
            (
                "<p>У TwoComms кастомний принт починається не з кнопки друку, а з перевірки задачі: яка річ, яка зона, який файл, який розмір і як усе це виглядатиме на реальній футболці чи худі. Саме тому в статтях ми ведемо до конфігуратора, але залишаємо поруч варіант написати менеджеру, якщо макет ще не готовий.</p>"
                if language == "uk"
                else "<p>В TwoComms кастомная печать начинается не с кнопки печати, а с проверки задачи: какая вещь, какая зона, какой файл, какой размер и как всё это будет выглядеть на реальной футболке или худи. Поэтому в статьях мы ведём в конфигуратор, но оставляем рядом вариант написать менеджеру, если макет ещё не готов.</p>"
            ),
            '<div class="article-keyword-cloud">',
        ]
    )
    for label, url in article["links"]:
        pieces.append(f'<a href="{url}">{_text(label, language)}</a>')
    pieces.append("</div>")
    return "\n".join(pieces)


def _cards_payload(items: list[tuple[str, str, str]]) -> list[dict]:
    return [{"label": {"uk": label, "ru": label}, "value": value, "caption": {"uk": caption, "ru": caption}} for label, value, caption in items]


def _localized_cards(items_by_lang: dict[str, list[tuple[str, str, str]]]) -> list[dict]:
    cards = []
    for index in range(len(items_by_lang["uk"])):
        uk_label, uk_value, uk_caption = items_by_lang["uk"][index]
        ru_label, ru_value, ru_caption = items_by_lang["ru"][index]
        cards.append(
            {
                "label": L(uk_label, ru_label),
                "value": uk_value or ru_value,
                "caption": L(uk_caption, ru_caption),
            }
        )
    return cards


@lru_cache(maxsize=12)
def _cover_font(size: int, bold: bool = False):
    candidates = [
        "/usr/share/fonts/truetype/dejavu/DejaVuSans-Bold.ttf" if bold else "/usr/share/fonts/truetype/dejavu/DejaVuSans.ttf",
        "/System/Library/Fonts/Supplemental/Arial Bold.ttf" if bold else "/System/Library/Fonts/Supplemental/Arial.ttf",
        "/Library/Fonts/Arial Unicode.ttf",
    ]
    for candidate in candidates:
        if candidate and Path(candidate).exists():
            return ImageFont.truetype(candidate, size=size)
    return ImageFont.load_default()


def _cover_text_lines(draw: ImageDraw.ImageDraw, text: str, font, max_width: int, max_lines: int = 3) -> list[str]:
    lines: list[str] = []
    current = ""
    for word in text.split():
        probe = f"{current} {word}".strip()
        if draw.textbbox((0, 0), probe, font=font)[2] <= max_width:
            current = probe
            continue
        if current:
            lines.append(current)
        current = word
        if len(lines) >= max_lines:
            break
    if current and len(lines) < max_lines:
        lines.append(current)
    if len(lines) == max_lines and " ".join(lines) != text:
        lines[-1] = lines[-1].rstrip(".,:;") + "..."
    return lines


def _draw_gradient(base: Image.Image) -> None:
    draw = ImageDraw.Draw(base)
    for y in range(base.height):
        ratio = y / max(base.height - 1, 1)
        red = int(8 + ratio * 10)
        green = int(11 + ratio * 8)
        blue = int(18 + ratio * 14)
        draw.line([(0, y), (base.width, y)], fill=(red, green, blue))
    glow = Image.new("RGBA", base.size, (0, 0, 0, 0))
    glow_draw = ImageDraw.Draw(glow)
    glow_draw.ellipse((940, -180, 1840, 700), fill=(251, 146, 60, 42))
    glow_draw.ellipse((-220, 520, 720, 1240), fill=(56, 189, 248, 34))
    glow_draw.ellipse((520, 180, 1380, 1040), fill=(34, 197, 94, 20))
    base.alpha_composite(glow.filter(ImageFilter.GaussianBlur(72)))


def _article_visual_kind(slug: str) -> str:
    if "hudi" in slug:
        return "hoodie"
    if "foto" in slug or "memu" in slug:
        return "photo"
    if "maket" in slug:
        return "layout"
    if "rozmir" in slug or "oversize" in slug:
        return "measure"
    if "dtf-dtg" in slug:
        return "compare"
    if "dtf-druk" in slug:
        return "dtf"
    if "logotypom" in slug or "brendu" in slug:
        return "brand"
    if "instagram" in slug:
        return "shop"
    if "podarunok" in slug:
        return "gift"
    return "shirt"


