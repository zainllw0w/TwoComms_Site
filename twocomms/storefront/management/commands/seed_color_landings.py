class Command(BaseCommand):
    help = "Seed published color-category landings for the SEO molecular-upgrade US-9."

    def add_arguments(self, parser):
        parser.add_argument(
            "--apply",
            action="store_true",
            help="Actually create / update landings (default is dry-run).",
        )
        parser.add_argument(
            "--unpublish-missing",
            action="store_true",
            help=(
                "Unpublish any color-landing rows that are not in the seed list. "
                "Useful for cleaning up old draft landings before going live."
            ),
        )

    def handle(self, *args, **opts):
        from productcolors.models import Color
        from storefront.models import Category, CategoryColorLanding

        apply_changes = bool(opts.get("apply"))
        unpublish_missing = bool(opts.get("unpublish_missing"))

        category_cache: Dict[str, Optional[Category]] = {}
        color_cache: Dict[str, Optional[Color]] = {}
        created = updated = skipped = 0
        seeded_keys: List[Tuple[int, str]] = []

        for entry in LANDINGS:
            cat_slug = entry["category_slug"]
            color_name = entry["color_name"]

            if cat_slug not in category_cache:
                category_cache[cat_slug] = Category.objects.filter(slug=cat_slug).first()
            category = category_cache[cat_slug]
            if category is None:
                self.stdout.write(self.style.WARNING(
                    f"[skip] category '{cat_slug}' not found"
                ))
                skipped += 1
                continue

            if color_name not in color_cache:
                color_cache[color_name] = Color.objects.filter(name=color_name).first()
            color = color_cache[color_name]
            if color is None:
                self.stdout.write(self.style.WARNING(
                    f"[skip] color '{color_name}' not found"
                ))
                skipped += 1
                continue

            existing = CategoryColorLanding.objects.filter(
                category=category, color=color,
            ).first()
            payload = dict(entry)
            payload.pop("category_slug")
            payload.pop("color_name")

            if existing is None:
                action = "create"
            else:
                action = "update"

            self.stdout.write(
                f"[{action}] {cat_slug} / {color_name} "
                f"(editorial: {len(payload['editorial_html'])} chars, "
                f"faq: {len(payload['faq_items'])} items)"
            )

            if not apply_changes:
                continue

            with transaction.atomic():
                if existing is None:
                    landing = CategoryColorLanding(
                        category=category,
                        color=color,
                        is_published=True,
                        **payload,
                    )
                    landing.full_clean()
                    landing.save()
                    created += 1
                else:
                    for k, v in payload.items():
                        setattr(existing, k, v)
                    existing.is_published = True
                    existing.full_clean()
                    existing.save()
                    updated += 1
                seeded_keys.append((category.id, payload.get("seo_title", "")))

        if unpublish_missing and apply_changes:
            seeded_pairs = {
                (e["category_slug"], e["color_name"]) for e in LANDINGS
            }
            for landing in CategoryColorLanding.objects.filter(is_published=True):
                key = (
                    landing.category.slug,
                    getattr(landing.color, "name", ""),
                )
                if key not in seeded_pairs:
                    landing.is_published = False
                    landing.save(update_fields=["is_published"])
                    self.stdout.write(self.style.WARNING(
                        f"[unpublish] {key[0]} / {key[1]}"
                    ))

        if apply_changes:
            self.stdout.write(self.style.SUCCESS(
                f"Done. created={created} updated={updated} skipped={skipped}"
            ))
        else:
            self.stdout.write(self.style.NOTICE(
                "Dry-run complete. Re-run with --apply to write changes."
            ))
