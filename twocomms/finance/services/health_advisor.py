from decimal import Decimal

ZERO = Decimal('0')


def _money(v):
    try:
        v = Decimal(str(v))
    except Exception:
        return '0 ₴'
    whole = f'{abs(v):,.0f}'.replace(',', ' ')
    return f'{whole} ₴'


